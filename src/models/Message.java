package models;

public class Message {

	private int type;
	private String message;


	public Message() {
	
	}

	public void setType(int messageType) {
		type = messageType;
	}

	public int getType() {
		return type;
	}
	



	public String getMessage() {
		return message;
	}
	



	public void setMessage(String message) {
		this.message = message;
	}

	
	
	
}
